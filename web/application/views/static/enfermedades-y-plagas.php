    <h3>&iquest;C&oacute;mo controlamos las Plagas y Enfermedades?</h3>
    <p>En las huertas familiares el control de plagas y enfermedades es relativamente f&aacute;cil si preparamos adecuadamente el suelo, le agregamos abonos debidamente compostados, y cultivamos una gran diversidad de hortalizas, plantas medicinales, plantas arom&aacute;ticas y de jard&iacute;n. Estos factores ayudar&aacute;n a evitar la mayor&iacute;a de sus problemas. Los principales problemas ser&aacute;n ocasionados por las hormigas, los caracoles y los bichos bolita los cuales se podr&aacute;n controlar por m&eacute;todos naturales o mec&aacute;nicos (Trampa de la bolsa h&uacute;meda para atrapar caracoles y bicho bolita y arroz blanco partido o macerado de ajo para el control de las hormigas).</p>

