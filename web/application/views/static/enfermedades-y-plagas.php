    <h3>&iquest;Qu&eacute; hacer cuando aparecen las plagas?</h3>
    <p>Usamos productos de origen natural, ya sea de origen vegetal o mineral que controlar&aacute;n las plagas y enfermedades sin afectar a los insectos que son beneficiosos para nuestra Huerta Familiar. Estos productos los preparamos en nuestra casa y tienen un muy bajo costo. Las formas de prepararlos son:</p>
    <ol>
      <li><strong>Maceraci&oacute;n:</strong> Es cuando dejamos las plantas uno o dos d&iacute;as remojadas en agua para extraer los ingredientes activos en un recipiente con tapa. Utilizamos hierbas frescas o secas y no deben fermentar.</li>
      <li><strong>Decocci&oacute;n:</strong> (Hervir) Las plantas se deben hervir en un recipiente con tapa por 10 minutos. Se deja enfriar tapada para que los ingredientes activos no se volatilicen.</li>
      <li><strong>Infusi&oacute;n:</strong> Misma preparaci&oacute;n que la decocci&oacute;n. Cuando hierve el agua quitar del fuego, agregar el producto y tapar.</li>
    </ol>

