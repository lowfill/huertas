    <h4>El suelo es el lugar donde nacen las semillas y crecen los cultivos. Posee el agua, el aire y los nutrientes necesarios para la vida de las plantas.</h4>

    <p>Las formas de laboreo que se utilicen van a modificar las caracter&iacute;sticas del suelo a lo largo del tiempo. Por lo tanto, de acuerdo a nuestras condiciones agroecol&oacute;gicas utilizaremos aquella t&eacute;cnica de cultivo que resulte m&aacute;s recomendable: canteros, surcos y/o camas altas. Ver M&eacute;todos de Siembra.</p>

    <h3>Preparaci&oacute;n de canteros</h3>
    <p>
      Son espacios de tierra preparada, elevada entre 20 - 25 cm, mullida y con terrones finos, ideal para el crecimiento de las semillas o plantas. Los armamos de aproximadamente 1 m de ancho y varios metros de largo seg&uacute;n las posibilidades. Entre los canteros se marcan caminos de 0,5 m de ancho para moverse con comodidad en funci&oacute;n del objetivo de la huerta. <br>
      La mejor orientaci&oacute;n de los canteros es la de norte a sur.
    </p>
    <p>
      Puede armarse as&iacute;:
    </p>
    <ul>
      <li>Marcamos con un hilo un rect&aacute;ngulo de entre 1 y 1,20 m de ancho por el largo disponible.</li>
      <li>Limpiamos los pastos de la superficie con una azada y los llevamos a la abonera.</li>
      <li>Aflojamos la tierra con la pala de dientes sacando las ra&iacute;ces que acumulamos junto con los pastos.</li>
      <li>Cuando terminamos de aflojar todo el cantero, con una pala recta cargamos la tierra de los costados y la volcamos hacia el centro del cantero. Debe quedar un par de cm m&aacute;s alto en el centro que en los bordes.</li>
      <li>Podemos afirmar los bordes con golpecitos de pala o azada para que no se desmoronen.</li>
      <li>Afinamos los terrones con azada o con el rastrillo.</li>
      <li>Abonamos agregando una capa de compost de 5 cm o con abono org&aacute;nico fresco (en este caso tenemos que esperar unos 30 d&iacute;as para sembrar).</li>
      <li>Cubrimos con una fina capa de pasto seco y dejamos sin cultivar por unos 15 &oacute; 20 d&iacute;as para que nazcan las malezas haciendo m&aacute;s f&aacute;cil su eliminaci&oacute;n.</li>
      <li>Pasado ese per&iacute;odo, retiramos la cubierta vegetal, rastrillamos y eliminamos las malezas.</li>
    </ul>
    Ahora s&iacute; podemos <strong>sembrar</strong> o <strong>plantar</strong>.
